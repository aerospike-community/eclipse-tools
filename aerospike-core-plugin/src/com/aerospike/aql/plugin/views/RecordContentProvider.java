package com.aerospike.aql.plugin.views;

import org.eclipse.jface.viewers.IStructuredContentProvider;
import org.eclipse.jface.viewers.Viewer;

import com.aerospike.client.query.KeyRecord;

public class RecordContentProvider implements IStructuredContentProvider {

	@Override
	public void dispose() {

	}

	@Override
	public void inputChanged(Viewer viewer, Object oldInput, Object newInput) {

	}

	@Override
	public Object[] getElements(Object inputElement) {
		KeyRecord keyRecord = (KeyRecord) inputElement;
		return new Object[]{keyRecord.key.digest, keyRecord.record.generation, keyRecord.record.expiration};
	}

}
